package model;

public class penjualan {
    
    private String nojual;
    private String tgljual;
    private String nofaktur;
    private Integer totaljual;
    private String nopemesanan;
    private String kodebarang;
    private Integer qtyjual;
    private Double subjual;

    public String getNojual() {
        return nojual;
    }

    public void setNojual(String nojual) {
        this.nojual = nojual;
    }

    public String getTgljual() {
        return tgljual;
    }

    public void setTgljual(String tgljual) {
        this.tgljual = tgljual;
    }

    public String getNofaktur() {
        return nofaktur;
    }

    public void setNofaktur(String nofaktur) {
        this.nofaktur = nofaktur;
    }

    public Integer getTotaljual() {
        return totaljual;
    }

    public void setTotaljual(Integer totaljual) {
        this.totaljual = totaljual;
    }

    public String getNopemesanan() {
        return nopemesanan;
    }

    public void setNopemesanan(String nopemesanan) {
        this.nopemesanan = nopemesanan;
    }

    public String getKodebarang() {
        return kodebarang;
    }

    public void setKodebarang(String kodebarang) {
        this.kodebarang = kodebarang;
    }

    public Integer getQtyjual() {
        return qtyjual;
    }

    public void setQtyjual(Integer qtyjual) {
        this.qtyjual = qtyjual;
    }

    public Double getSubjual() {
        return subjual;
    }

    public void setSubjual(Double subjual) {
        this.subjual = subjual;
    }

}




